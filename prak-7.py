import numpy as np

# веб-ресурсы с оценками пользователей
resources = ['https://www.goods.ru/', 'https://www.book.ru/', 'https://www.movies.ru/']

# списки оценок по каждой единице оценивания
ratings = {'Товар': [8, 9, 10, 8, 7, 9, 10, 8, 9, 10],
           'Книга': [7, 7, 8, 9, 9, 9, 10, 10, 8, 7],
           'Фильм': [8, 7, 9, 7, 8, 10, 9, 8, 9, 10],
           'Песня': [9, 10, 7, 8, 7, 10, 8, 9, 8, 9],
           'Картина': [10, 8, 7, 9, 10, 7, 8, 9, 10, 7],
           'Комикс': [7, 9, 8, 8, 9, 10, 7, 8, 7, 9],
           'Игра': [9, 8, 10, 9, 7, 8, 7, 9, 8, 10],
           'Порода животного': [8, 9, 7, 10, 7, 9, 8, 9, 10, 7],
           'Школа': [10, 9, 8, 7, 9, 8, 10, 7, 9, 10],
           'Репетитор': [7, 8, 9, 7, 10, 8, 9, 8, 10, 9],
           'Отель': [9, 10, 7, 8, 9, 7, 8, 9, 10, 7],
           'Курорт': [8, 7, 9, 7, 8, 10, 9, 8, 9, 10]}

while True:
    print('Выберите единицу оценивания: ', ratings.keys())
    unit = input()
    if unit in ratings:
        break
    else:
        print('Выбранной единицы оценивания нет в списке.')

while True:
    print('Выберите период для расчета среднего рейтинга: Дата, Неделя, Месяц, Полугодие, Общий, Возраст')
    period = input()
    if period in ['Дата', 'Неделя', 'Месяц', 'Полугодие', 'Общий', 'Возраст']:
        break
    else:
        print('Выбранный период неверен.')

# расчет среднего рейтинга по каждой единице оценивания
avg_ratings = []
for i in range(len(ratings[unit])):
    r = []
    for j in range(len(resources)):
        r.append(ratings[unit][i])
    avg_ratings.append(np.mean(r))

# вывод среднего рейтинга по периоду
if period == 'Дата':
    print('Средний рейтинг за день: ', np.mean(avg_ratings))
elif period == 'Неделя':
    print('Средний рейтинг за неделю: ', np.mean(avg_ratings) * 7)
elif period == 'Месяц':
    print('Средний рейтинг за месяц: ', np.mean(avg_ratings) * 30)
elif period == 'Полугодие':
    print('Средний рейтинг за полугодие: ', np.mean(avg_ratings) * 180)
elif period == 'Общий':
    print('Средний общий рейтинг: ', np.mean(avg_ratings))
elif period == 'Возраст':
    age = input('Введите возраст пользователя: ')
    if age.isdigit():
        print('Средний рейтинг для возраста ', age, ': ', np.mean(avg_ratings))
    else:
        print('Некорректный ввод возраста.')
